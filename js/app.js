let currentLang = localStorage.getItem("dc-hostel-lang") || "en";
if (!i18n[currentLang]) {
  currentLang = "en";
}
let activeStudentId = null;

const byId = (id) => document.getElementById(id);

function t(key) {
  return key.split(".").reduce((value, part) => value && value[part], i18n[currentLang]) || key;
}

function studentValue(student, field) {
  return currentLang === "bn" && student.bn && student.bn[field] ? student.bn[field] : student[field];
}

function initNavbar() {
  const navbar = byId("navbar");
  if (!navbar) return;

  const handleScroll = () => {
    navbar.classList.toggle("scrolled", window.scrollY > 24);
  };

  handleScroll();
  window.addEventListener("scroll", handleScroll, { passive: true });
}

function initTheme() {
  const toggle = byId("toggle");
  const savedTheme = localStorage.getItem("dc-hostel-theme");

  if (savedTheme === "dark") {
    document.body.classList.add("dark");
  }

  if (!toggle) return;

  toggle.setAttribute("aria-label", t("common.toggleDark"));
  toggle.addEventListener("click", () => {
    document.body.classList.toggle("dark");
    localStorage.setItem("dc-hostel-theme", document.body.classList.contains("dark") ? "dark" : "light");
  });
}

function initLanguage() {
  const buttons = document.querySelectorAll("[data-lang-option]");

  buttons.forEach((button) => {
    button.addEventListener("click", () => {
      currentLang = button.dataset.langOption;
      localStorage.setItem("dc-hostel-lang", currentLang);
      applyLanguage();
      renderDynamicContent();
      refreshAos();
    });
  });

  applyLanguage();
}

function applyLanguage() {
  const page = document.body.dataset.page || "home";

  document.documentElement.lang = currentLang;
  document.body.dataset.lang = currentLang;
  document.title = t(`pageTitle.${page}`);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = t(element.dataset.i18n);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    element.setAttribute("placeholder", t(element.dataset.i18nPlaceholder));
  });

  document.querySelectorAll("[data-lang-option]").forEach((button) => {
    button.classList.toggle("active", button.dataset.langOption === currentLang);
  });

  const toggle = byId("toggle");
  if (toggle) {
    toggle.setAttribute("aria-label", t("common.toggleDark"));
  }

  document.querySelectorAll(".modal-close").forEach((button) => {
    button.setAttribute("aria-label", t("common.closeProfile"));
  });
}

function studentMatches(student, query) {
  const normalizedQuery = query.toLowerCase().trim();
  if (!normalizedQuery) return true;

  const values = [
    student.name,
    student.room,
    student.phone,
    student.email,
    student.address,
    student.blood,
    student.hobby,
    student.goal,
    student.bio,
    student.bn?.name,
    student.bn?.address,
    student.bn?.hobby,
    student.bn?.goal,
    student.bn?.bio
  ].filter(Boolean).join(" ").toLowerCase();

  return values.includes(normalizedQuery);
}

function getFilteredStudents() {
  const search = byId("studentSearch");
  if (!search) return students;
  return students.filter((student) => studentMatches(student, search.value));
}

function renderStudentCards(list = getFilteredStudents()) {
  const grid = byId("studentGrid");
  if (!grid) return;

  const limit = Number(grid.dataset.limit || list.length);
  const visibleStudents = list.slice(0, limit);

  if (!visibleStudents.length) {
    grid.innerHTML = `<div class="empty-state">${t("common.noStudent")}</div>`;
    return;
  }

  grid.innerHTML = visibleStudents.map((student, index) => `
    <button class="student-card" type="button" data-student-id="${student.id}" data-aos="fade-up" data-aos-delay="${Math.min(index * 70, 280)}">
      <img src="${student.img}" alt="${studentValue(student, "name")}" loading="lazy">
      <span class="student-body">
        <h3>${studentValue(student, "name")}</h3>
        <p>${t("common.room")} ${student.room}</p>
        <span class="student-meta">
          <span class="pill">${student.blood}</span>
          <span class="pill">${studentValue(student, "goal")}</span>
        </span>
      </span>
    </button>
  `).join("");
}

function initStudentSearch() {
  const search = byId("studentSearch");
  if (!search) return;

  search.addEventListener("input", () => {
    renderStudentCards();
    refreshAos();
  });
}

function groupByRoom() {
  return students.reduce((rooms, student) => {
    rooms[student.room] = rooms[student.room] || [];
    rooms[student.room].push(student);
    return rooms;
  }, {});
}

function renderRoommates() {
  const grid = byId("roommateGrid");
  if (!grid) return;

  const rooms = Object.entries(groupByRoom());
  const limit = Number(grid.dataset.limit || rooms.length);
  const visibleRooms = rooms.slice(0, limit);

  grid.innerHTML = visibleRooms.map(([room, pair], index) => {
    const left = pair[0];
    const right = pair[1];

    return `
      <article class="room-pair" data-aos="fade-up" data-aos-delay="${index * 90}">
        ${renderRoomPerson(left, "fade-right")}
        <div class="room-number">${t("common.room")}<br>${room}</div>
        ${right ? renderRoomPerson(right, "fade-left") : `<div class="room-person empty">${t("common.emptySeat")}</div>`}
      </article>
    `;
  }).join("");
}

function renderRoomPerson(student, animation) {
  return `
    <button class="room-person" type="button" data-student-id="${student.id}" data-aos="${animation}">
      <img src="${student.img}" alt="${studentValue(student, "name")}" loading="lazy">
      <strong>${studentValue(student, "name")}</strong>
      <span>${t("common.room")} ${student.room}</span>
    </button>
  `;
}

function renderResults() {
  const table = byId("resultTable");
  if (!table) return;

  table.innerHTML = students.map((student) => `
    <tr>
      <td><button type="button" data-student-id="${student.id}">${studentValue(student, "name")}</button></td>
      <td>${student.room}</td>
      <td>${student.result.gpa.toFixed(2)}</td>
      <td>${student.result.physics}</td>
      <td>${student.result.chemistry}</td>
      <td>${student.result.math}</td>
    </tr>
  `).join("");
}

function renderDynamicContent() {
  renderStudentCards();
  renderRoommates();
  renderResults();

  if (activeStudentId) {
    const student = students.find((item) => item.id === activeStudentId);
    if (student) openStudentModal(student);
  }
}

function initStudentClicks() {
  document.addEventListener("click", (event) => {
    const trigger = event.target.closest("[data-student-id]");
    if (!trigger) return;

    const student = students.find((item) => item.id === Number(trigger.dataset.studentId));
    if (student) openStudentModal(student);
  });
}

